#     Interpreter:
#         Translates and executes code line by line.
#         Doesn't produce a separate executable file.
#         Example: Python interpreter.

#     Compiler:
#         Translates entire source code into machine code or intermediate code.
#         Produces a standalone executable file or bytecode.
#         Examples: GCC for C/C++, Java compiler (javac).

#     Runtime Environment:
#         Provides essential services during program execution.
#         Manages memory, I/O operations, etc.
#         Examples: Java Virtual Machine (JVM), Common Language Runtime (CLR).

# In short:

#     Interpreter runs code line by line.
#     Compiler translates entire code before execution.
#     Runtime environment supports program execution.